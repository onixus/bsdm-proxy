export function formatEnv(form: ConfigFormState): string {
  const config = collectConfig(form)
  const lines = [
    '# Generated by BSDM Admin Console',
    '# See packaging/config/bsdm-proxy.env.example for full reference',
    '',
  ]
  const written = new Set<string>()

  for (const key of ENV_ORDER) {
    if (config[key] !== undefined && config[key] !== '') {
      lines.push(`${key}=${config[key]}`)
      written.add(key)
    }
  }
  for (const [key, value] of Object.entries(config)) {
    if (!written.has(key) && value !== '') {
      lines.push(`${key}=${value}`)
    }
  }

  return lines.join('\n') + '\n'
}

