export function generateAclRules(form: ConfigFormState): { default_action: string; rules: object[] } | null {
  if (!form.aclEnabled) return null

  const rules: object[] = []
  let priority = 100

  const addCategory = (id: string, name: string, category: string, enabled: boolean) => {
    if (!enabled) return
    rules.push({
      id,
      name,
      enabled: true,
      priority: priority++,
      action: 'deny',
      rule_type: { Category: category },
      redirect_url: null,
      comment: null,
    })
  }

  addCategory('block-malware', 'Block malware URLs', 'malware', form.aclBlockMalware)
  addCategory('block-phishing', 'Block phishing URLs', 'phishing', form.aclBlockPhishing)
  addCategory('block-adult', 'Block adult content', 'adult', form.aclBlockAdult)
  addCategory('block-gambling', 'Block gambling sites', 'gambling', form.aclBlockGambling)

  return { default_action: form.aclDefaultAction, rules }
}

export function downloadFile(filename: string, content: string): void {
  const blob = new Blob([content], { type: 'text/plain' })
  const url = URL.createObjectURL(blob)
  const a = document.createElement('a')
  a.href = url
  a.download = filename
  document.body.appendChild(a)
  a.click()
  document.body.removeChild(a)
  URL.revokeObjectURL(url)
}
