import { useState, useEffect } from 'react'

export type Language = 'ru' | 'en'

const LANG_KEY = 'bsdm_console_lang'

export function getInitialLanguage(): Language {
  const saved = localStorage.getItem(LANG_KEY)
  if (saved === 'ru' || saved === 'en') return saved
  return 'ru' // Russian as primary default
}

export function setLanguage(lang: Language) {
  localStorage.setItem(LANG_KEY, lang)
  window.dispatchEvent(new CustomEvent('bsdm-lang-change', { detail: lang }))
}

export function useLanguage(): [Language, (lang: Language) => void] {
  const [lang, setLangState] = useState<Language>(getInitialLanguage)

  useEffect(() => {
    const handleLangChange = (e: Event) => {
      const newLang = (e as CustomEvent<Language>).detail
      setLangState(newLang)
    }
    window.addEventListener('bsdm-lang-change', handleLangChange)
    return () => window.removeEventListener('bsdm-lang-change', handleLangChange)
  }, [])

  const updateLang = (newLang: Language) => {
    setLangState(newLang)
    setLanguage(newLang)
  }

  return [lang, updateLang]
}

